package com.demo.lastika.model;

import javax.persistence.*;

@Entity
@Table(name = "employee")
public class Employee {

    @Id
    private String id;

    @Column
    private String name;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
